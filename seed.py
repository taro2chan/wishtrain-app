def build_member_numbers():
    rows = list(BASE_MEMBER_NUMBERS)
    all_members = [m[0] for m in MEMBERS]
    team1 = {n for n, num in BASE_MEMBER_NUMBERS if num in (2, 3)}
    team2 = {n for n, num in BASE_MEMBER_NUMBERS if num in (4, 5)}
    rows += [(n, 6) for n in team1]
    rows += [(n, 7) for n in team2]
    rows += [(n, 8) for n in all_members]
    rows += [(n, 9) for n in all_members]
    return sorted(set(rows))

def reset_tables(conn):
    conn.execute("DELETE FROM rehearsal_numbers")
    conn.execute("DELETE FROM member_numbers")
    conn.execute("DELETE FROM number_musics")
    conn.execute("DELETE FROM rehearsals")
    conn.execute("DELETE FROM numbers")
    conn.execute("DELETE FROM musics")
    conn.execute("DELETE FROM members")

def seed():
    init_db()
    with get_db() as conn:
        reset_tables(conn)
        conn.executemany("INSERT INTO members VALUES (?, ?, ?)", MEMBERS)
        conn.executemany("INSERT INTO numbers VALUES (?, ?, ?, ?)", NUMBERS)
        conn.executemany("INSERT INTO musics VALUES (?, ?, ?)", MUSICS)
        conn.executemany("INSERT INTO number_musics VALUES (?, ?, ?)", NUMBER_MUSICS)
        conn.executemany("INSERT INTO member_numbers VALUES (?, ?)", build_member_numbers())
        conn.executemany(
            "INSERT INTO rehearsals VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)",
            REHEARSALS
        )
        conn.executemany(
            "INSERT INTO rehearsal_numbers VALUES (?, ?, ?, ?, ?)",
            REHEARSAL_NUMBERS
        )
        conn.commit()
    print("seed 完了")

